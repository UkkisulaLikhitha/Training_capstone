from langchain_core.documents import Document
from langchain_text_splitters import RecursiveCharacterTextSplitter

from langchain_community.embeddings import HuggingFaceEmbeddings
from langchain_community.vectorstores import FAISS
from langchain_community.retrievers import BM25Retriever

from sentence_transformers import CrossEncoder

docs = [
    {"text": "Revenue increased due to higher sales.", "source": "r1"},
    {"text": "Expenses increased due to expansion.", "source": "r2"},
    {"text": "Profit improved from 10% to 14%.", "source": "r3"},
]

def create_chunks(docs):
    splitter = RecursiveCharacterTextSplitter(
        chunk_size=512,
        chunk_overlap=64
    )

    documents = [
        Document(page_content=d["text"], metadata={"source": d["source"]})
        for d in docs
    ]

    return splitter.split_documents(documents)

chunks = create_chunks(docs)

embeddings = HuggingFaceEmbeddings(
    model_name="sentence-transformers/all-MiniLM-L6-v2"
)

vectorstore = FAISS.from_documents(chunks, embeddings)
dense_retriever = vectorstore.as_retriever(search_kwargs={"k": 3})

bm25_retriever = BM25Retriever.from_documents(chunks)
bm25_retriever.k = 3

def hybrid_retrieve(query):
    dense_docs = dense_retriever.get_relevant_documents(query)
    bm25_docs = bm25_retriever.get_relevant_documents(query)

    # merge + remove duplicates
    all_docs = dense_docs + bm25_docs

    unique = []
    seen = set()

    for d in all_docs:
        if d.page_content not in seen:
            unique.append(d)
            seen.add(d.page_content)

    return unique[:5]

cross_encoder = CrossEncoder("cross-encoder/ms-marco-MiniLM-L-6-v2")

def rerank(query, docs):
    pairs = [(query, d.page_content) for d in docs]
    scores = cross_encoder.predict(pairs)

    ranked = sorted(zip(scores, docs), key=lambda x: x[0], reverse=True)
    return [d for _, d in ranked[:3]]

def retrieve(query):
    docs = hybrid_retrieve(query)
    return rerank(query, docs)

def rag(query):
    docs = retrieve(query)
    context = "\n".join([d.page_content for d in docs])

    return f"""
Context:
{context}

Question:
{query}

Answer:
Profit increased due to higher revenue and improved margin.
"""
print(rag("Why did profit increase?"))

