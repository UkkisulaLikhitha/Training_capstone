class TokenBucket:

    def __init__(self, capacity=5, refill_rate=1):
        self.capacity = capacity
        self.tokens = capacity
        self.refill_rate = refill_rate
        self.last_refill = time.time()

    def consume(self, amount=1):

        current_time = time.time()

        elapsed = current_time - self.last_refill

        self.tokens = min(
            self.capacity,
            self.tokens + elapsed * self.refill_rate
        )

        self.last_refill = current_time

        if self.tokens >= amount:
            self.tokens -= amount
            return True

        return False


bucket = TokenBucket(
    capacity=5,
    refill_rate=1
)

# ----------------------------------------------------------
# ENHANCED INSTRUMENTED CALL
# ----------------------------------------------------------

def instrumented_call(
    prompt,
    model=MODEL_ROUTINE,
    system="",
    max_retries=3,
    **kw
):

    global CURRENT_COST

    retries = 0

    with span("llm.call", model=model) as sp:

        while retries <= max_retries:

            try:

                # --------------------------
                # RATE LIMITING
                # --------------------------

                while not bucket.consume():
                    time.sleep(0.5)

                # --------------------------
                # LLM CALL
                # --------------------------

                response = call_claude(
                    prompt,
                    model=model,
                    system=system,
                    **kw
                )

                # --------------------------
                # COST TRACKING
                # --------------------------

                CURRENT_COST += response.cost_usd

                if CURRENT_COST > BUDGET_LIMIT:

                    log_event(
                        "budget.exceeded",
                        level="ERROR",
                        current_cost=CURRENT_COST,
                        budget_limit=BUDGET_LIMIT
                    )

                    raise RuntimeError(
                        f"Budget exceeded. "
                        f"Current cost = ${CURRENT_COST:.6f}"
                    )

                # --------------------------
                # TELEMETRY
                # --------------------------

                sp.attributes.update({

                    "input_tokens":
                        response.input_tokens,

                    "output_tokens":
                        response.output_tokens,

                    "cost_usd":
                        response.cost_usd,

                    "latency_ms":
                        response.latency_ms,

                    "stop_reason":
                        response.stop_reason,

                    "retry_count":
                        retries,

                    "running_cost":
                        CURRENT_COST
                })

                LLM_CALLS.append({

                    "model":
                        model,

                    "cost_usd":
                        response.cost_usd,

                    "latency_ms":
                        response.latency_ms,

                    "retry_count":
                        retries
                })

                return response

            except Exception as e:

                retries += 1

                if retries > max_retries:
                    raise

                wait_time = 2 ** retries

                log_event(
                    "llm.retry",
                    level="WARN",
                    retry_count=retries,
                    wait_seconds=wait_time,
                    error=str(e)
                )

                time.sleep(wait_time)

