def approve_refund(order_id):

    approval = input(
        f"Approve refund for Order {order_id}? (yes/no): "
    )

    if approval.lower()!="yes":

        return "Refund Rejected"

    return "Refund Approved"

# ==================================================
# ROUTER
# ==================================================

def router(query):

    q = query.lower()

    if "order" in q:
        return "order"

    elif "refund" in q:
        return "refund"

    elif "email" in q:
        return "email"

    else:
        return "faq"

# ==================================================
# MAIN AGENT
# ==================================================

def support_agent(
    session_id,
    query
):

    cached = get_cache(query)

    if cached:

        trace("CACHE HIT")

        return cached

    trace("CACHE MISS")

    save_chat(
        session_id,
        "user",
        query
    )

    memories = recall_memory(query)

    trace(
        f"MEMORIES: {memories}"
    )

    intent = router(query)

    trace(
        f"ROUTED TO: {intent}"
    )

    if intent=="order":

        result = retry(
            order_status,
            102
        )

    elif intent=="refund":

        result = approve_refund(
            102
        )

        if "Approved" in result:

            send_email_async(
                "customer@test.com",
                "Refund processed"
            )

    elif intent=="email":

        result = send_email_async(
            "customer@test.com",
            query
        )

    else:

        result = faq_tool(query)

    save_chat(
        session_id,
        "assistant",
        str(result)
    )

    store_memory(query)

    set_cache(
        query,
        result
    )

    return result

