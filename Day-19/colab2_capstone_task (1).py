# PROMPT CACHE
# ==================================================

cache = {}

def get_cache(prompt):

    key = hashlib.md5(
        prompt.encode()
    ).hexdigest()

    return cache.get(key)

def set_cache(prompt,response):

    key = hashlib.md5(
        prompt.encode()
    ).hexdigest()

    cache[key] = response

# ==================================================
# TRACE LOGGING
# ==================================================

trace_logs=[]

def trace(msg):

    trace_logs.append(msg)

    print(f"[TRACE] {msg}")

# ==================================================
# RETRIES
# ==================================================

def retry(func,*args,retries=3):

    for attempt in range(retries):

        try:
            return func(*args)

        except Exception as e:

            print(
                f"Retry {attempt+1}: {e}"
            )

            time.sleep(1)

    raise Exception(
        "Max retries exceeded"
    )

