from sentence_transformers import SentenceTransformer

# ==================================================
# REDIS SHORT TERM MEMORY
# ==================================================

redis_client = fakeredis.FakeRedis()

def save_chat(session, role, msg):
    redis_client.rpush(
        f"chat:{session}",
        f"{role}: {msg}"
    )

def get_chat(session):
    msgs = redis_client.lrange(
        f"chat:{session}",
        0,
        -1
    )
    return [m.decode() for m in msgs]

# ==================================================
# VECTOR STORE LONG TERM MEMORY
# ==================================================

embedder = SentenceTransformer(
    "all-MiniLM-L6-v2"
)

dimension = 384
index = faiss.IndexFlatL2(dimension)

memory_docs = []

def store_memory(text):

    vec = embedder.encode([text]).astype("float32")

    index.add(vec)

    memory_docs.append(text)

def recall_memory(query,k=3):

    if len(memory_docs)==0:
        return []

    vec = embedder.encode([query]).astype("float32")

    D,I = index.search(vec,k)

    results=[]

    for idx in I[0]:
        if idx < len(memory_docs):
            results.append(memory_docs[idx])

    return results

